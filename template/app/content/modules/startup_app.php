		<h3>How does GreenTec invest?</h3>
<p>GreenTec will apply its time and resources to helping your company achieve your mutually-agreed business goals including funding support. We will work closely with your team to understand and improve your business case, while working with your team to help them improve their operations and your company’s investment posture. We will also work to provide your company with network and technological partnerships as appropriate and assist with acquiring public-sector support such as grants as well as equity investments</p>
<p>&nbsp;</p>
<h3>What does “Results4Equity” mean?</h3>
<p>GreenTec’s support contracts are structured under our “Results4Equity” model. GreenTec Capital is awarded equity its partner companies only in the case of achieving our mutually-agreed business goals. This means that we only benefit when our portfolio companies benefit and aligns our interests over the long-term.</p>
<p>&nbsp;</p>
<h3>What is “Impact Investing”?</h3>
<p>Impact Investing” refers to investments made to achieve both positive social and environmental impacts alongside a financial return.</p>
<p>&nbsp;</p>
<h3>What sectors do you invest in?</h3>
<p>We focus on agriculture, digitalization, and sustainable resources, with a strong preference for impactful and technology driven solutions.</p>
<p>&nbsp;</p>
<h3>What countries do you invest in?</h3>
<p>We are currently investing in Rwanda, Ghana, Uganda, Nigeria, Benin, and Kenya. We apply a proprietary risk tool to evaluate investments which factors in international public domain information such as ease of doing business and political transparency data.</p>
<p>&nbsp;</p>

                </div>
                <div class="col-12 col-md-6 second-col text-padding-x">



		<h3>What do you mean by “Proof of Concept”</h3>
<p>By “Proof of Concept,” we mean a developed business solution taking or ready to take revenues.</p>
<p>&nbsp;</p>
<h3>What is „Capacity Building“?</h3>
<p>By “Capacity Building”, we refer to the process of improving a company’s operations and business fundamentals. One of our Company Builders will work closely with your team to understand your business and improve its operations and business model jointly.</p>
<p>&nbsp;</p>
<h3>What do your services cost?</h3>
<p>GreenTec does not take payment for its services directly and is only awarded equity in its partner’s stock when a mutually agreed-KPI is achieved. GreenTec will receive small fees on grant or investment funding raised.</p>
<p>&nbsp;</p>
<h3>How much will GreenTec give to my Company?</h3>
<p>We typically do not make direct cash investments, but provide fund raising support as KPI in our support services. GreenTec supports its partner companies with funding support as appropriate to the partner company’s needs.</p>
<p>&nbsp;</p>
<h3>My business is not in Africa, should I apply for support?</h3>
<p>You should apply for support if your business is based in Africa or does business in Africa.</p>
