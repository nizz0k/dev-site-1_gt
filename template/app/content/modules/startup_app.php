		<p class="lead">We invest in African start-ups and SMEs that have the ability to scale up and be sustainable as well as create positive impact in the communities in which they operate. We consider applications for funding from businesses which fulfil our investment criteria as below</p>
<p><strong>Geography:</strong> We support African start-ups and SMEs, as well as, international companies doing business in Africa. We evaluate each investment opportunity individually, applying proprietary risk assessment tools and careful due diligence to assure a good fit within our portfolio. We are primarily focused on Sub-Saharan Africa and are currently invested in Ghana, Nigeria, Uganda, Rwanda, Benin, and Kenya.</p>
<p><strong>Sectors of focus:</strong> With a strong focus on impactful local solutions, we broadly invest into agriculture, digitalization, and sustainable resources.</p>
<p><span style="font-weight: normal !msorm;"><strong>Business Profile</strong></span>: We support start-ups and SMEs with the following profiles</p>
<ul>
<li>proof-of-concept/first revenues</li>
<li>Management team with sector experience</li>
<li>Impact focused</li>
<li>Scalable business model</li>
</ul>
<p>GreenTec considers start-ups and SMEs doing business related to our focus areas outlined above.</p>
<p><strong>Investment Size</strong>: Our investments are structured as a package of services to investees to assist them with achieving mutually-agreed business goals and funding goals.</p>

                </div>
                <div class="col-12 col-md-6 second-col text-padding-x">



		<p><strong>Duration of Support:</strong> We approach investments with a mid-term perspective (3-5 years) that allows innovative concepts to develop their business fundamentals and demonstrate investment readiness.</p>
<p><strong>Entrepreneur Profile:</strong> We seek to empower visionary entrepreneurs who wish to grow their business and expand their positive impacts. Entrepreneurs should be committed full-time and have prior business or entrepreneurial experience. We seek entrepreneurs who are willing to work with us as value-adding partners and share management information regularly.</p>
<p><strong>Results4Equity Investments:</strong> GreenTec Capital Partners provides support through individually tailored packages designed to help the company achieve mutually-agreed and pre-defined business goals (KPI’s) based around company development and financing. At the successful completion of those goals, GreenTec Capital Partners will be awarded a pre-agreed share percentage of the investee’s company. We believe this presents a cash-friendly and low-risk proposition for start-ups, that aligns our interests in the long-term success and growth of the company.</p>
<p><strong>Social Impact:</strong> GreenTec maintains a portfolio of at least 75% impact-oriented investments and thus has a strong preference for companies explicitly impact focused and contributing to the UN’s Sustainable Development Goals.</p>
<p><strong>Capacity Building:</strong> GreenTec’s support includes Capacity Building for all of our start-ups and SMEs. A GreenTec Company Builder will work closely with your team and integrate his skills to your company’s development. We will work to improve your business model and company operations to achieve scalable growth and investment readiness. Due to the close collaboration between teams, it will be important for start-up teams to be receptive to receiving advice and implementing recommendations.</p>

